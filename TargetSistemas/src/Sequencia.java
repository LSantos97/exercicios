public class Sequencia {
    public static void main(String[] args) {
        int numA = 7;
        int nextA = numA + 2;
        System.out.println(nextA);

        int numB = 64;
        int nextB = numB * 2;
        System.out.println(nextB);

        int numC = 7;
        int nextC = numC * numC;
        System.out.println(nextC);

        int numD = 64;
        int nextD = numD + 20;
        System.out.println(nextD);

        int numE = 5;
        int numLast = 8;
        int nextE = numE + numLast;
        System.out.println(nextE);

        int numF = 19;
        int nextF = numF + (8 - 6);
        System.out.println(nextF);


    }
}
